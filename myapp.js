function prevNext() {

	if(document.getElementById("elevator_details").style.display === "block"){
		document.getElementById("initial_title").style.display = "none";
		document.getElementById("elevator_details").style.display = "none";
		document.getElementById("sketch_details").style.display = "none";
    	document.getElementById("hidesign_details").style.display = "block";
		document.getElementById("title_text").innerHTML = "Hidesign: Recycled Leather and Brass Product Design";
		
	} else if (document.getElementById("hidesign_details").style.display === "block") {
		document.getElementById("initial_title").style.display = "none";
		document.getElementById("hidesign_details").style.display = "none";
		document.getElementById("elevator_details").style.display = "none";
		document.getElementById("sketch_details").style.display = "block";
		document.getElementById("title_text").innerHTML = "Hand-drawn Sketch Projects";
	
	} else {
		document.getElementById("initial_title").style.display = "none";
		document.getElementById("sketch_details").style.display = "none";
		document.getElementById("hidesign_details").style.display = "none";
		document.getElementById("elevator_details").style.display = "block";
		document.getElementById("title_text").innerHTML = "Leg Elevator for Ankle Fracture Rehabilitation";
	}
}

